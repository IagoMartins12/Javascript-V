/*As APIs Restfulls são nada mais do que APIs que seguem um padrão especifico de construção 
elas devem ter: 
Tem que seguir a arquitetura cliente-servidor 
Necessita ter uma conexão HTTP
Comunicação stateless, ou seja, todas as comunicações ocorrem de formas separadas e independentes
A interface deve ser uniforme e todas as informações devem ser padronizadas 
*/

/*Metodos de uma api: 
Get: Obtem informações
Post: Envia as informações
Put/patch: Atualiza as informações
Delete: Deleta as informações

Exemplos: 
get/users : Obtem as informações dos usuarios
post/users : Envia as informações dos usuarios
put/users/99 : Atualiza o usuario 99
get/users/99 : Obtem as informações apenas do usuario 99
delete/users/99 : Deleta as informações do usuario 99*/ 